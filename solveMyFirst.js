//โจทย์ให้ทำฟังก์ชัน solveMefirst รับค่า 2 ตัวคือ a และ b
function solveMeFirst(a, b) {
  sum = a + b; // สร้างตัวแปรชื่อ sum มีค่าเท่ากับ a+b และให้
  return sum; // ให้ return sum หรือ a+b นั่นละออกมา
}
console.log(solveMeFirst(1, 2)); // ให้ปริ้นฟังก์ชัน solveMeFirst ซึ่ง รับค่า a=1 และ b=2
const c = solveMeFirst(1, 2); // ลองอีกแบบคือ ประกาศตัวแปร c มีค่าเท่ากับ การกระทำจาก solveMeFirst
console.log(c); // ปริ้น c

